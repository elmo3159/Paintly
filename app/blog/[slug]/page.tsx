        // H4見出し
        if (line.startsWith('#### ')) {
          return `<h4 class="text-lg md:text-xl font-bold text-gray-900 mb-3 mt-6">${line.slice(5)}</h4>`
        }
        // リスト
        if (line.startsWith('- ')) {
          return `<li class="ml-6 mb-2">${line.slice(2)}</li>`
        }
        // 水平線
